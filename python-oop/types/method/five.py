class calc:
    @staticmethod
    def add(a,b):
        return a+b
    
    @staticmethod
    def sub(a,b):
        
        return a-b
    @staticmethod
    def multi(a,b):
        return a*b
    
    
    @staticmethod
    def divi(a,b):
        return a/b
    
    
print(calc.add(10,20))
print(calc.sub(10,20))
print(calc.multi(10,20))
print(calc.divi(10,20))